				<h2 class = "center">FAQ</h2>
				<h4>CREATING AN ACCOUNT</h4>
				<p><b>I am a Cornell employee or student. When I log in using my NetID, I get an error message that 
				says an account has already been created using my email. I am certain I've never logged in, so how do I create an account!?</b><br/>
				Not to worry--someone else isn't using your email (the NetID login wouldn't allow that.) 
				This error happens when an account was created using your Cornell email by an administrative person 
				to issue you tickets in the past. If you get this error message, please email 
				<a href="mailto:questions@baileytickets.com">questions@baileytickets.com</a> and
				we'll fix the problem and write back to let you know that you can go ahead and create your account.</p>
				<p><b>I am a Cornell alumnus or an employee of a Cornell-affiliated organization. Should I log-in
				under the Cornell Community section?</b><br/>
				At this time, the Cornell log-kn (which is authenticated by NetID) is for current faculty, staff, 
				and students only. Please use a non-Cornell email address to register under the General Public section.</p>
				<p><b>I'm already registered at CornellTickets.com. Do I need to create a new registration for BaileyTickets.com?</b><br/>
				Yes. Although the two sites are both Cornell sites and powered by the same company, UniversityTickets, they are two
				separate databases, so you will have to create a new login for each site.</p>
				<p><b>Why do I have to give my billing address when creating an account, even if the tickets are free?</b><br/>
				In order to create an account on BailetTickets.com, the system requires you to enter your billing and shipping
				addresses. You can make changes to your address at any time by logging in to your account. When you do make 
				a ticket purchase on that account, it matches the billing address with the credit card you use. For security
				reasons, BaileyTickets.com <b>does not</b> save your credit card information, so you will have to enter that
				each time you make a purchase.</p>
				<h4>BUYING YOUR TICKETS</h4>
				<p><b>My credit card isn't working.</b><br/>
				Make sure to eliminate all spaces between numbers. Also, be sure that your billing address and zip code
				match the credit card. If the problems persist, please email <a href="mailto:questions@baileytickets.com">questions@baileytickets.com</a>.</p>
				<p><b>Why can't I get the Cornell discount over the phone or in person [Cornell Concert Series only]?</b><br/>
				The system was created to give a discount using NetID authentication. This is a perk for the Cornell community
				that we have never had before - please take advantage of it online!</p>
				<p><b>I want to attend an event at Bailey, but it's not listed on BaileyTickets.com. Where can I find 
				information about this event?</b><br/>
				Certain events presented by Cornell Music or various other departments are not ticketed, and some events
				require free tickets but are only available in person (usually at Willard Straight Hall.) You can find
				all Bailey event listings, with admission information, at <a href = "http://baileyhall.cornell.edu">http://baileyhall.cornell.edu</a>.</p>
				<p><b>I am going with 3 friends who are not Cornell employees or students. How can I purchase tickets so that
				we are all sitting together?</b><br/>
				As with any ticket purchase in the industry, if you want to be certain that you sit with friends, purchase
				all your tickets at once. As a Cornell employee or student, you are eligible to purchase up to 4 tickets
				for each concert using your Cornell discount.</p>
				<p><b>I want to organize a group of 10 or more to go to a concert at Bailey Hall. Are there discounts, and
				how do I set this up?</b><br/>
				Group rates are often available for events at Bailey if you bring 10 or more people. For Cornell Concert Series
				events, contact their administrative office at (607)255-5144 or <a href="mailto:info@cornellconcertseries.com">info@cornellconcertseries.com</a>. 
				To inquire about other events, contact the Bailey Box Office at <a href="mailto:questions@baileytickets.com">questions@baileytickets.com</a> 
				or (607)254-3374.</p>
